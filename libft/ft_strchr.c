#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (ft_isascii(c) == 0)
		return (NULL);
	while (*s != c)
	{
		if (*s == '\0')
			return (NULL);
		s++;
	}
	return ((char*)s);
}
